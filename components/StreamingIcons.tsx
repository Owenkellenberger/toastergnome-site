export default function StreamingIcons({ links, className = "" }: Props) {
  const entries = order
    .map((service) => [service, links[service]] as const)
    .filter(([, url]) => Boolean(url));

  if (entries.length === 0) return null;

  return (
    <div className={`flex items-center justify-center gap-2.5 ${className}`}>
      {entries.map(([service, url]) => (
        <a
          key={service}
          href={url}
          target="_blank"
          rel="noreferrer noopener"
          aria-label={labels[service]}
          className="inline-flex items-center justify-center w-9 h-9 rounded-full bg-charcoal text-white hover:bg-gold transition-colors"
        >
          <Icon service={service} />
        </a>
      ))}
    </div>
  );
}
